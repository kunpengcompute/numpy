/*
 * Target-specific compilation (e.g. SVE, AVX2).
 * NPY_CPU_DISPATCH_CURFX(func_name) produces e.g. func_name_SVE.
 * Uses HWY_STATIC_DISPATCH for Highway's sub-target selection.
 */
#define SUM_DISPATCH(func_name, simd_func, type) \
NPY_VISIBILITY_HIDDEN type \
NPY_CPU_DISPATCH_CURFX(func_name)(const type *a, npy_intp n) \
{ \
    return HWY_STATIC_DISPATCH(simd_func)( \
        reinterpret_cast<const type *>(a), n); \
}

#define COMPLEX_SUM_DISPATCH(func_name, simd_func, type) \
NPY_VISIBILITY_HIDDEN void \
NPY_CPU_DISPATCH_CURFX(func_name)(type *rr, type *ri, \
                                   const type *a, npy_intp n) \
{ \
    HWY_STATIC_DISPATCH(simd_func)(rr, ri, \
        reinterpret_cast<const type *>(a), n); \
}

#else
/*
 * Baseline compilation: plain func_name is the caller-facing entry point.
 * Uses HWY_STATIC_DISPATCH directly since all SIMD targets benefit.
 * Highway automatically selects best available instruction set
 * (e.g. NEON on baseline ARM64, SSE2/AVX on baseline x86).
 */
#define SUM_DISPATCH(func_name, simd_func, type) \
NPY_VISIBILITY_HIDDEN type \
NPY_CPU_DISPATCH_CURFX(func_name)(const type *a, npy_intp n) \
{ \
    return HWY_STATIC_DISPATCH(simd_func)( \
        reinterpret_cast<const type *>(a), n); \
}

#define COMPLEX_SUM_DISPATCH(func_name, simd_func, type) \
NPY_VISIBILITY_HIDDEN void \
NPY_CPU_DISPATCH_CURFX(func_name)(type *rr, type *ri, \
                                   const type *a, npy_intp n) \
{ \
    HWY_STATIC_DISPATCH(simd_func)(rr, ri, \
        reinterpret_cast<const type *>(a), n); \
}

#endif

