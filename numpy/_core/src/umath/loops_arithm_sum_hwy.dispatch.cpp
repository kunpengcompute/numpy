/**
 * Highway SIMD-optimized pairwise summation for float/double and complex.
 *
 * Uses NumPy's CPU dispatch system (NPY_CPU_DISPATCH_CURFX) to compile
 * multiple target variants (SVE, ASIMD, NEON for ARM; X86_V4/V3/V2 for x86).
 * Within each variant, Highway's HWY_STATIC_DISPATCH handles sub-target
 * selection at runtime (e.g. SVE vs SVE2, AVX2 vs AVX-512).
 *
 * Baseline (plain name, e.g. npy_highway_pairwise_sum_f32_contig) acts as a
 * trampoline: it uses HWY_STATIC_DISPATCH directly since pairwise sum
 * benefits all SIMD targets including baseline NEON/AVX.
 *
 * Algorithm:
 * - Small arrays (n < 4*lanes): scalar loop with -0.0 init for correct signs
 * - Medium arrays (n <= 256): 4-way SIMD unroll with independent accumulators
 * - Large arrays: pairwise recursion aligned to unroll_len for SIMD efficiency
 */

